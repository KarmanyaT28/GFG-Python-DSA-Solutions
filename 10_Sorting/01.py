# Sorting in python

# sorted() function


num = [6,5,4,8,3,2]

sortedNum = sorted(num)

print(sortedNum)

# Time Complexity: O(n log(n))
# Space Complexity: O(n)



# sort() function

num1 = [3,5,7,8,2,1,2,4,5,7,8]

num1.sort()

print(num1)


# Time Complexity: O(n log(n))
# Space Complexity: O(1)




